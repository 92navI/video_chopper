package com.navi92.util;

public class TextFormat {
    public static final String RESET = "\u001B[0m";
    public static final String BLACK = "\u001B[30m";
    public static final String RED = "\u001B[31m";
    public static final String GREEN = "\u001B[32m";
    public static final String YELLOW = "\u001B[33m";
    public static final String BLUE = "\u001B[34m";
    public static final String PURPLE = "\u001B[35m";
    public static final String CYAN = "\u001B[36m";
    public static final String WHITE = "\u001B[37m";

    public static final String BOLD = "\u001B[1m";
    public static final String BOLD_R = "\u001B[22m";

    public static final String ITALIC = "\u001B[3m";
    public static final String ITALIC_R = "\u001B[23m";

    public static final String DIM = "\u001B[2m";
    public static final String DIM_R = "\u001B[22m";

    public static final String UNDERLINE = "\u001B[4m";
    public static final String UNDERLINE_R = "\u001B[24m";

    public static final String STRIKETHROUGH = "\u001B[9m";
    public static final String STRIKETHROUGH_R = "\u001B[29m";
}